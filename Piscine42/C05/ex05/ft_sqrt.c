int	ft_sqrt(int nb)
{
	int	j;

	j = 0;
	if (nb == -2147483648)
		return (0);
	if (nb < 0)
		nb = -nb;
	while (j * j < nb && j <= 46340)
	{
		j++;
		if (nb == j * j)
			return (j);
	}
	return (0);
}
